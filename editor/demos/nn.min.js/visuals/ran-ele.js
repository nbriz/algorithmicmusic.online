// Tone.js Synth (for demonstration)
const synth = new Tone.Synth().toDestination()

const words = ['freq', 'pitch', 'note', 'speed', 'Hz']

function clickWord () {
  // change the word's color
  const clr = nn.randomColor()
  this.css('color', clr)
  // replay frequency
  const f = this.data.freq
  const d = this.data.dur
  synth.triggerAttackRelease(f, d)
}

function playAndDraw () {
  // change the HTML <body> background color randomly
  const x = nn.random(0, nn.width)
  const y = nn.random(0, nn.height)
  const txt = nn.random(words)
  const size = nn.random(25, 100)
  const word = nn.create('div')
    .position(x, y)
    .content(txt)
    .css({
      cursor: 'pointer',
      userSelect: 'none',
      fontSize: size
    })
    .addTo('body')
    .on('click', clickWord)

  // play a random frequency for a random duration
  const f = nn.random(220, 880)
  const d = nn.random(0.1, 0.5)
  synth.triggerAttackRelease(f, d)

  // store the frequency and duration in the word's data
  word.data.freq = f
  word.data.dur = d
}

nn.create('button')
  .content('new word')
  .addTo('body')
  .on('click', playAndDraw)
