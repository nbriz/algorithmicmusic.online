/*
  The example below asks the user for permission to use their camera feed, and if they grant it it creates a <video> element with the camera feed's stream as it's source.
*/

let video // global variable for our video

async function setup () {
  // create the video element with our camera feed
  video = nn.create('video')
    .addTo('body')
    .set({
      autoplay: true,
      muted: true,
      stream: await nn.askFor({ video: true })
    })
}

nn.on('load', setup)
