const wave = createWaveform()
const osc = new Tone.Oscillator()
osc.set({
  frequency: 440,
  type: 'triangle'
})
osc.toDestination()
osc.connect(wave)

function play () {
  osc.start()
  osc.stop('+0.5')
}

// we can create HTML elements with "nn"
nn.create('button')     // what we want to make
  .content('play tone') // what we want it to say
  .addTo('body')       // where we want to put it
  .on('click', play)   // what we want it to do
