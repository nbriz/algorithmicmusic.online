const wave = viz.createWaveform()
const osc = new Tone.Oscillator()
osc.set({
  frequency: 440,
  type: 'sine'
})
osc.toDestination()
osc.connect(wave)

function toggle (e) {
  const val = e.target.checked
  if (val === true) osc.start()
  else osc.stop()
}

function changeType (e) {
  const val = e.target.value
  osc.set({ type: val })
}


// we can create a drop down list with the <select> element
nn.create('select')
  .set({
    options: ['sine', 'square', 'triangle', 'sawtooth']
  })
  .addTo('body')
  .on('change', changeType)


nn.create('input')
  .set({ type: 'checkbox' })
  .addTo('body')
  .on('change', toggle)

nn.create('label')
  .content('toggle on/off')
  .addTo('body')
