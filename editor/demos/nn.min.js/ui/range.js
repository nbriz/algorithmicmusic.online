const wave = createWaveform()
const osc = new Tone.Oscillator()
osc.set({
  frequency: 440,
  type: 'triangle'
})
osc.toDestination()
osc.connect(wave)

function toggle (e) {
  const val = e.target.checked
  if (val === true) osc.start()
  else osc.stop()
}

function changePitch (e) {
  const val = e.target.value
  osc.set({ frequency: val })
}

// we can create a slider by setting the <input> type to "range"
nn.create('input')
  .set({
    type: 'range',
    min: 220, // minimum value for this slider
    max: 880, // maximum value for this range
    value: 440, // default value
    step: 0.1 // smallest change we can make when we slide it
  })
  .addTo('body')
  .on('input', changePitch)

nn.create('input')
  .set({ type: 'checkbox' })
  .addTo('body')
  .on('change', toggle)

nn.create('label')
  .content('toggle on/off')
  .addTo('body')
