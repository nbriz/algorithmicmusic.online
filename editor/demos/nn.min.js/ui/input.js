const wave = createWaveform()
const osc = new Tone.Oscillator()
osc.set({
  frequency: 440,
  type: 'custom',
  partials: [] // updated by the input field
})
osc.toDestination()
osc.connect(wave)

function play () {
  osc.start()
  osc.stop('+0.5')
}

function textInput (e) {
  const val = e.target.value.split(',')
  osc.set({ partials: val })
}

// the HTML <input> can be used to write text fields
nn.create('input')
  .addTo('body')
  .on('input', textInput)

  nn.create('button')
    .content('play tone')
    .addTo('body')
    .on('click', play)
