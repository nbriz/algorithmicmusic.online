const wave = createWaveform()
const osc = new Tone.Oscillator()
osc.set({
  frequency: 440,
  type: 'triangle'
})
osc.toDestination()
osc.connect(wave)

function toggle (e) {
  const val = e.target.checked
  if (val === true) osc.start()
  else osc.stop()
}

// the HTML <input> can be used to write text fields
nn.create('input')
  .set({ type: 'checkbox' })
  .addTo('body')
  .on('change', toggle)

// we can also create a text label this way
nn.create('label')
  .content('toggle on/off')
  .addTo('body')
