// we can use our keyboard to trigger events as well.
// click the right side of this page (where the wave is) to "focus" it, and then press any key on your keyboard
const wave = createWaveform()
const synth = new Tone.Synth().toDestination()
synth.connect(wave)

function play () {
  synth.triggerAttackRelease(440, 0.25)
}

// "keydown" fires everytime you press a key on your keyboard
nn.on('keydown', play)
