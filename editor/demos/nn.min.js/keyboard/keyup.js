// click the right side of this page (where the wave is) to "focus" it, and then press and hold any key on your keyboard
const wave = createWaveform()
const synth = new Tone.Synth().toDestination()
synth.connect(wave)

// to keep track of whether the synth is playing or not
let isPlaying = false

function attack () {
  // without this conditional check the attack will keep fireing over and over while the key is pressed down
  if (isPlaying === false) {
    synth.triggerAttack(440)
    isPlaying = true
  }
}

function release () {
  synth.triggerRelease()
  isPlaying = false
}


nn.on('keydown', attack)
nn.on('keyup', release)
