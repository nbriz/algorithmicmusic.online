// click the right side of this page (where the wave is) to "focus" it, and then press the keys "a" through "k"
const synth = new Tone.Synth().toDestination()
const wave = createWaveform()
synth.connect(wave)

// we can write conditional logic using the event object which tells us which key was pressed
function play (e) {
  if (e.key === 'a') {
    synth.triggerAttackRelease(440, 0.25)
  } else if (e.key === 's') {
    synth.triggerAttackRelease(493.88, 0.25)
  } else if (e.key === 'd') {
    synth.triggerAttackRelease(523.25, 0.25)
  } else if (e.key === 'f') {
    synth.triggerAttackRelease(587.33, 0.25)
  } else if (e.key === 'g') {
    synth.triggerAttackRelease(659.26, 0.25)
  } else if (e.key === 'h') {
    synth.triggerAttackRelease(698.46, 0.25)
  } else if (e.key === 'j') {
    synth.triggerAttackRelease(783.99, 0.25)
  } else if (e.key === 'k') {
    synth.triggerAttackRelease(880, 0.25)
  }
}

nn.on('keydown', play)
